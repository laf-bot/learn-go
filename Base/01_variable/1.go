package main

import "fmt"

/**
* variable 变量
**/

func main() {

	var a string = "laf"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	// 变量声明

	// 声明一个变量并初始化
	var aa = "RUNOOB"
	fmt.Println(aa)

	// int 类型 没有初始化为零值
	var bb int
	fmt.Println(bb)

	// bool 零值为false

	var cc bool
	fmt.Println(cc)

}
